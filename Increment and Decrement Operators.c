#include <stdio.h>
int main(){
    int num1 = 5, num2 = 10;
    printf("Original value of num1: %d\n", num1);
    printf("Original value of num2: %d\n", num2);
    // Increment operator (++) for num1
    num1++;
    printf("After incrementing num1, it is now: %d\n", num1);  
    // Decrement operator (--) for num2
    num2--;
    printf("After decrementing num2, it is now: %d\n", num2);
    return 0;}
